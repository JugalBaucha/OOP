import java.util.*;
class Mul14{
    public static void main(String[]args)
    {
        int i,j;
        for(i=1;i<=10;i++)
        {
            for(j=1;j<=10;j++)
            {
                int result=i*j;
                System.out.println(i+"*"+j+"="+result);
            }
            System.out.println("\n");
        }
    }
}
